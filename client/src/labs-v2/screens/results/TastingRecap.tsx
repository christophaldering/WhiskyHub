import { useState, useMemo } from "react";
import { BarChart, Bar, XAxis, YAxis, Tooltip, ResponsiveContainer, Cell } from "recharts";
import jsPDF from "jspdf";
import { saveJsPdf } from "@/lib/pdf";
import type { ThemeTokens } from "../../tokens";
import { SP, FONT, RADIUS } from "../../tokens";
import type { Translations } from "../../i18n";

interface RecapData {
  tasting: {
    id: string;
    title: string;
    date?: string;
    location?: string;
    hostName?: string;
  };
  participantCount: number;
  whiskyCount: number;
  topRated: { name: string; distillery?: string; avgScore: number }[];
  mostDivisive: { name: string; stddev: number } | null;
  overallAverages: { nose: number; taste: number; finish: number; overall: number };
  participantHighlights: { name: string; ratingsCount: number; avgScore: number }[];
}

interface Props {
  th: ThemeTokens;
  t: Translations;
  recap: RecapData;
}

const MEDAL_COLORS = ["#FFD700", "#C0C0C0", "#CD7F32"];
const BAR_COLORS = ["#D9A15B", "#C97845", "#9C6A5E", "#7F8C5A"];

export default function TastingRecap({ th, t, recap }: Props) {
  const [copyDone, setCopyDone] = useState(false);

  const avgData = useMemo(() => [
    { dimension: t.ratingNose, value: recap.overallAverages.nose },
    { dimension: t.ratingPalate, value: recap.overallAverages.taste },
    { dimension: t.ratingFinish, value: recap.overallAverages.finish },
    { dimension: t.ratingOverall, value: recap.overallAverages.overall },
  ], [recap, t]);

  const buildShareText = () => {
    const lines: string[] = [];
    lines.push(recap.tasting.title);
    if (recap.tasting.date) lines.push(recap.tasting.date);
    if (recap.tasting.location) lines.push(recap.tasting.location);
    lines.push("");
    lines.push(`${t.resTopRated}:`);
    (recap.topRated || []).slice(0, 3).forEach((w, i) => {
      lines.push(`  #${i + 1} ${w.name} - ${w.avgScore.toFixed(1)}`);
    });
    lines.push("");
    const avg = recap.overallAverages;
    lines.push(`${t.ratingNose}: ${avg.nose.toFixed(1)} | ${t.ratingPalate}: ${avg.taste.toFixed(1)} | ${t.ratingFinish}: ${avg.finish.toFixed(1)} | ${t.ratingOverall}: ${avg.overall.toFixed(1)}`);
    lines.push("");
    lines.push("- CaskSense");
    return lines.join("\n");
  };

  const handleShare = async () => {
    const text = buildShareText();
    if (navigator.share) {
      try {
        await navigator.share({ title: recap.tasting.title, text });
      } catch {}
    } else {
      await navigator.clipboard.writeText(text);
      setCopyDone(true);
      setTimeout(() => setCopyDone(false), 2000);
    }
  };

  const handlePrint = () => window.print();

  const handlePdf = () => {
    const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
    const pw = doc.internal.pageSize.getWidth();
    const ph = doc.internal.pageSize.getHeight();
    const margin = 18;
    const cw = pw - margin * 2;
    let y = 18;
    const accent: [number, number, number] = [212, 162, 86];
    const dark: [number, number, number] = [30, 30, 32];
    const muted: [number, number, number] = [120, 120, 125];
    const bg: [number, number, number] = [250, 248, 245];

    const drawBg = () => { doc.setFillColor(...bg); doc.rect(0, 0, pw, ph, "F"); doc.setFillColor(...accent); doc.rect(0, 0, pw, 3, "F"); };
    const drawFooter = () => {
      doc.setDrawColor(...accent); doc.setLineWidth(0.3); doc.line(margin, ph - 16, pw - margin, ph - 16);
      doc.setFontSize(7); doc.setTextColor(...muted); doc.text("Generated by CaskSense", margin, ph - 12);
    };

    drawBg();

    doc.setFontSize(10); doc.setTextColor(...accent); doc.setFont("helvetica", "bold");
    doc.text("CaskSense", pw / 2, y, { align: "center" }); y += 10;

    doc.setFontSize(22); doc.setTextColor(...dark);
    doc.text(recap.tasting.title, pw / 2, y, { align: "center" }); y += 12;

    doc.setDrawColor(...accent); doc.setLineWidth(0.5); doc.line(margin, y, pw - margin, y); y += 12;

    if ((recap.topRated || []).length > 0) {
      doc.setFontSize(15); doc.setFont("helvetica", "bold"); doc.setTextColor(...dark);
      doc.text(t.resTopRated, margin, y); y += 10;
      recap.topRated.slice(0, 5).forEach((w, i) => {
        if (y + 14 > ph - 20) { drawFooter(); doc.addPage(); drawBg(); y = 18; }
        doc.setFont("helvetica", "bold"); doc.setFontSize(12);
        doc.setTextColor(...(i < 3 ? accent : dark));
        doc.text(`${i + 1}.  ${w.name}`, margin + 4, y);
        doc.setFontSize(14); doc.setTextColor(...accent);
        doc.text(w.avgScore.toFixed(1), pw - margin, y, { align: "right" });
        y += 10;
      });
      y += 4;
    }

    if (recap.mostDivisive) {
      doc.setFontSize(15); doc.setFont("helvetica", "bold"); doc.setTextColor(...dark);
      doc.text(t.resMostDivisive, margin, y); y += 8;
      doc.setFont("helvetica", "normal"); doc.setFontSize(10); doc.setTextColor(...muted);
      doc.text(`${recap.mostDivisive.name} (${recap.mostDivisive.stddev.toFixed(2)})`, margin + 4, y); y += 12;
    }

    const dims = [
      { label: t.ratingNose, value: recap.overallAverages.nose },
      { label: t.ratingPalate, value: recap.overallAverages.taste },
      { label: t.ratingFinish, value: recap.overallAverages.finish },
      { label: t.ratingOverall, value: recap.overallAverages.overall },
    ];
    doc.setFontSize(15); doc.setFont("helvetica", "bold"); doc.setTextColor(...dark);
    doc.text(t.resAverages, margin, y); y += 10;
    const barMaxW = cw - 46;
    dims.forEach((d) => {
      doc.setFont("helvetica", "normal"); doc.setFontSize(9); doc.setTextColor(...muted);
      doc.text(d.label, margin + 4, y);
      doc.setFillColor(230, 225, 218); doc.roundedRect(margin + 36, y - 3.5, barMaxW, 5, 1.5, 1.5, "F");
      const bw = (d.value / 100) * barMaxW;
      doc.setFillColor(...accent); doc.roundedRect(margin + 36, y - 3.5, bw, 5, 1.5, 1.5, "F");
      doc.setFont("helvetica", "bold"); doc.setFontSize(9); doc.setTextColor(...dark);
      doc.text(d.value.toFixed(1), margin + 38 + barMaxW, y, { align: "left" });
      y += 8;
    });

    drawFooter();
    const slug = recap.tasting.title.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/-+$/, "");
    saveJsPdf(doc, `casksense-${slug}-recap.pdf`);
  };

  return (
    <div data-testid="tasting-recap" style={{ fontFamily: FONT.body, padding: SP.md }}>
      <style>{`
        @media print {
          body * { visibility: hidden; }
          [data-testid="tasting-recap"], [data-testid="tasting-recap"] * { visibility: visible; }
          [data-testid="tasting-recap"] { position: absolute; left: 0; top: 0; width: 100%; }
          .recap-actions { display: none !important; }
        }
      `}</style>

      <h2
        data-testid="text-recap-title"
        style={{ fontFamily: FONT.display, fontSize: 22, fontWeight: 700, color: th.text, margin: `0 0 ${SP.sm}px` }}
      >
        {t.resRecapTitle}
      </h2>
      <div style={{ fontSize: 13, color: th.muted, marginBottom: SP.lg }}>
        {recap.tasting.title}
        {recap.tasting.date && ` -- ${recap.tasting.date}`}
        {recap.tasting.location && ` -- ${recap.tasting.location}`}
      </div>

      {(recap.topRated || []).length > 0 && (
        <div
          data-testid="card-top-rated"
          style={{
            background: th.bgCard,
            borderRadius: RADIUS.lg,
            padding: SP.lg,
            border: `1px solid ${th.border}`,
            marginBottom: SP.md,
          }}
        >
          <h3 style={{ fontFamily: FONT.display, fontSize: 17, fontWeight: 600, color: th.text, margin: `0 0 ${SP.md}px` }}>
            {t.resTopRated}
          </h3>
          {recap.topRated.slice(0, 3).map((w, i) => (
            <div
              key={i}
              data-testid={`card-medal-${i}`}
              style={{
                display: "flex",
                alignItems: "center",
                gap: SP.md,
                padding: `${SP.sm}px 0`,
                borderBottom: i < 2 ? `1px solid ${th.border}` : "none",
              }}
            >
              <span
                style={{
                  width: 32,
                  height: 32,
                  borderRadius: RADIUS.full,
                  display: "flex",
                  alignItems: "center",
                  justifyContent: "center",
                  fontSize: 14,
                  fontWeight: 700,
                  background: MEDAL_COLORS[i],
                  color: i === 0 ? "#78350f" : i === 1 ? "#1f2937" : "#451a03",
                }}
              >
                {i + 1}
              </span>
              <div style={{ flex: 1, minWidth: 0 }}>
                <div style={{ fontSize: 15, fontWeight: 600, color: th.text, overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>
                  {w.name}
                </div>
                {w.distillery && (
                  <div style={{ fontSize: 12, color: th.muted }}>{w.distillery}</div>
                )}
              </div>
              <span style={{ fontSize: 20, fontWeight: 700, fontFamily: FONT.display, color: th.gold }}>
                {w.avgScore.toFixed(1)}
              </span>
            </div>
          ))}
        </div>
      )}

      {recap.mostDivisive && (
        <div
          data-testid="card-most-divisive"
          style={{
            background: th.bgCard,
            borderRadius: RADIUS.lg,
            padding: SP.md,
            border: `1px solid ${th.border}`,
            marginBottom: SP.md,
          }}
        >
          <h3 style={{ fontFamily: FONT.display, fontSize: 15, fontWeight: 600, color: th.text, margin: `0 0 ${SP.sm}px` }}>
            {t.resMostDivisive}
          </h3>
          <div style={{ fontSize: 15, fontWeight: 600, color: th.amber }}>{recap.mostDivisive.name}</div>
          <div style={{ fontSize: 12, color: th.muted }}>Std Dev: {recap.mostDivisive.stddev.toFixed(2)}</div>
        </div>
      )}

      <div
        data-testid="card-averages-chart"
        style={{
          background: th.bgCard,
          borderRadius: RADIUS.lg,
          padding: SP.md,
          border: `1px solid ${th.border}`,
          marginBottom: SP.md,
        }}
      >
        <h3 style={{ fontFamily: FONT.display, fontSize: 15, fontWeight: 600, color: th.text, margin: `0 0 ${SP.md}px` }}>
          {t.resAverages}
        </h3>
        <div style={{ height: 200 }}>
          <ResponsiveContainer width="100%" height="100%">
            <BarChart data={avgData} layout="vertical" margin={{ left: 60, right: 20 }}>
              <XAxis type="number" domain={[0, 100]} tick={{ fill: th.muted, fontSize: 11 }} axisLine={false} tickLine={false} />
              <YAxis type="category" dataKey="dimension" tick={{ fill: th.text, fontSize: 12 }} axisLine={false} tickLine={false} width={55} />
              <Tooltip
                contentStyle={{
                  background: th.bgCard,
                  border: `1px solid ${th.border}`,
                  borderRadius: RADIUS.sm,
                  color: th.text,
                  fontSize: 12,
                }}
              />
              <Bar dataKey="value" radius={[0, 4, 4, 0]} barSize={20}>
                {avgData.map((_, i) => (
                  <Cell key={i} fill={BAR_COLORS[i % BAR_COLORS.length]} />
                ))}
              </Bar>
            </BarChart>
          </ResponsiveContainer>
        </div>
      </div>

      {(recap.participantHighlights || []).length > 0 && (
        <div
          data-testid="card-participant-highlights"
          style={{
            background: th.bgCard,
            borderRadius: RADIUS.lg,
            padding: SP.md,
            border: `1px solid ${th.border}`,
            marginBottom: SP.md,
          }}
        >
          <h3 style={{ fontFamily: FONT.display, fontSize: 15, fontWeight: 600, color: th.text, margin: `0 0 ${SP.md}px` }}>
            {t.resParticipants}
          </h3>
          <div style={{ display: "grid", gridTemplateColumns: "repeat(auto-fill, minmax(140px, 1fr))", gap: SP.sm }}>
            {recap.participantHighlights.map((p, i) => (
              <div
                key={i}
                data-testid={`card-participant-${i}`}
                style={{
                  padding: SP.sm,
                  borderRadius: RADIUS.md,
                  background: th.bgHover,
                  textAlign: "center",
                }}
              >
                <div style={{ width: 32, height: 32, borderRadius: RADIUS.full, background: th.gold, color: th.bg, display: "flex", alignItems: "center", justifyContent: "center", margin: "0 auto", fontSize: 14, fontWeight: 700 }}>
                  {p.name.charAt(0).toUpperCase()}
                </div>
                <div style={{ fontSize: 13, fontWeight: 600, color: th.text, marginTop: SP.xs, overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>{p.name}</div>
                <div style={{ fontSize: 11, color: th.muted }}>{p.ratingsCount} {t.resRatings} -- Avg {p.avgScore.toFixed(1)}</div>
              </div>
            ))}
          </div>
        </div>
      )}

      <div className="recap-actions" style={{ display: "flex", gap: SP.sm, flexWrap: "wrap" }}>
        <button
          data-testid="button-recap-pdf"
          onClick={handlePdf}
          style={{
            padding: `${SP.sm}px ${SP.md}px`,
            borderRadius: RADIUS.md,
            border: `1px solid ${th.border}`,
            background: th.bgCard,
            color: th.text,
            fontSize: 13,
            fontWeight: 600,
            fontFamily: FONT.body,
            cursor: "pointer",
          }}
        >
          {t.resPdf}
        </button>
        <button
          data-testid="button-recap-share"
          onClick={handleShare}
          style={{
            padding: `${SP.sm}px ${SP.md}px`,
            borderRadius: RADIUS.md,
            border: `1px solid ${th.border}`,
            background: th.bgCard,
            color: th.text,
            fontSize: 13,
            fontWeight: 600,
            fontFamily: FONT.body,
            cursor: "pointer",
          }}
        >
          {copyDone ? t.resCopied : t.resShare}
        </button>
        <button
          data-testid="button-recap-print"
          onClick={handlePrint}
          style={{
            padding: `${SP.sm}px ${SP.md}px`,
            borderRadius: RADIUS.md,
            border: `1px solid ${th.border}`,
            background: th.bgCard,
            color: th.text,
            fontSize: 13,
            fontWeight: 600,
            fontFamily: FONT.body,
            cursor: "pointer",
          }}
        >
          {t.resPrint}
        </button>
      </div>
    </div>
  );
}
